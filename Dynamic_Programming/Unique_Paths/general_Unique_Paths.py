'''
演算法步驟：
1. 機器人只能往右或往下走，所以走到棋盤任何一格 `(i, j)` 的方法數，等於「走到它上面那一格的方法數加上左邊那一格的方法數」。
2. 最直覺且標準的做法是建立大小為 `m x n` 的 2D 陣列 `dp`，將第一排和第一列都初始化為 1（因為只能一直往同一方向走）。
3. 然而由於更新 `dp[i][j]` 只需要用到上方和左方的資訊，我們可以使用一維的「滾動陣列 DP」來節省空間。
4. 建立長度為 `n` 的陣列 `row`，全部初始化為 1。這代表我們掃描第一列的歷史資訊。
5. 進行 `m-1` 次的大迴圈（因為第一列已經是全 1 了）：
   - 在每個迴圈裡，從左到右更新陣列的每一格：`row[j] = row[j] + row[j-1]`。
   - 這裡右邊的 `row[j]` 是頭頂那格（舊的 `row[j]`），而 `row[j-1]` 是剛剛更新過的左邊那格。
6. 最後回傳陣列的最後一個元素 `row[-1]` 即可。空間 O(n)。
'''
class Solution:
    def uniquePaths(self, m: int, n: int) -> int:
        # 一維 DP 陣列滾動，空間為 O(n)
        row = [1] * n
        
        for i in range(1, m):
            for j in range(1, n):
                row[j] += row[j - 1]
                
        return row[-1]
