'''
演算法步驟：
1. 這是一道 DP 字串解析題。使用一個陣列 `dp`，`dp[i]` 代表字串前 `i` 個字元有幾種解碼方式。
2. 初始化：`dp[0] = 1` (空字串有 1 種空解法)，`dp[1] = 1` (如果第一個字不是 '0')。
3. 遍歷字串：
   - 檢查「單位數字」解碼 (s[i])：如果這個數字不是 '0'，代表它可以自己獨立被解碼，所以繼承 `dp[i-1]` 的解碼數。
   - 檢查「雙位數字」解碼 (s[i-1:i+1])：如果前一個數字與當前數字組合起來在 "10" 到 "26" 之間，代表可以組合成一個英文字母，便可繼承 `dp[i-2]` 的解碼數。
4. 每走一步就把它們加總起來，最後回傳 `dp[n]`（O(N) 空間）。
'''
class Solution:
    def numDecodings(self, s: str) -> int:
        if not s or s[0] == '0':
            return 0
            
        n = len(s)
        dp = [0] * (n + 1)
        dp[0] = 1
        dp[1] = 1
        
        for i in range(2, n + 1):
            # 檢查單一字元
            if s[i - 1] != '0':
                dp[i] += dp[i - 1]
                
            # 檢查雙字元
            two_digit = int(s[i - 2:i])
            if 10 <= two_digit <= 26:
                dp[i] += dp[i - 2]
                
        return dp[n]
